# ============================================================
# 文件：state.py
# 作用：管理“事件冷却时间”，防止识别层连发把设备刷爆
#
# 核心思想：
#   每种事件单独记一个“上次触发时间”
#   再来时拿“现在时间”减一下，看间隔够不够冷却时长
#   够了就放行 + 把“上次时间”更新为现在
#   不够就拦下
#
# 这一版是“最简单的内存版”：
#   - 数据存在程序内存里
#   - 服务一重启，记忆就清空（这对我们当前阶段完全够用）
#   - 以后真有需要，再升级成持久化版本
# ============================================================

import time


# ------------------------------------------------------------
# 内部状态：每个事件最后一次触发的时间戳
# ------------------------------------------------------------
# 这是一个字典：
#   key   -> 事件名，比如 "hurt"
#   value -> 上次触发的时间戳（time.time() 返回的浮点数，单位是秒）
#
# 变量名前面加下划线 _ 是 Python 的命名约定，意思是：
#   “这是模块内部用的变量，外面别直接动它”
# 不是强制规则，但能起到“提醒未来的你”的作用。
_last_trigger_time: dict[str, float] = {}


# ------------------------------------------------------------
# 对外函数：判断这个事件现在能不能触发
# ------------------------------------------------------------
def can_trigger(event_name: str, cooldown_seconds: float) -> bool:
    """
    判断某个事件“现在”是否允许触发。

    参数：
        event_name        : 事件名，比如 "hurt"
        cooldown_seconds  : 冷却秒数，由调用方决定（hurt 短一点，death 长一点）

    返回：
        True  -> 允许触发，同时把“上次触发时间”更新成现在
        False -> 还在冷却中，本次拒绝
    """

    # time.time() 返回当前时间的“时间戳”
    # 时间戳就是“从 1970-01-01 到现在过了多少秒”，是一个很大的浮点数
    # 我们不关心它的绝对值，只关心“两次时间戳相减得到的间隔”
    now = time.time()

    # 去字典里找“这个事件上次触发的时间”
    # 如果从来没触发过（字典里没这个 key），就当作 0
    # 0 距离“现在”一定大于任何冷却时长，所以第一次必然放行
    last = _last_trigger_time.get(event_name, 0)

    # 两次时间戳相减 = 距离上次触发过去了多少秒
    elapsed = now - last

    # 关键判断
    if elapsed >= cooldown_seconds:
        # 通过冷却检查：把“上次时间”更新为现在，下次再来时基于这个新值判断
        _last_trigger_time[event_name] = now
        return True

    # 还在冷却期内：直接拒绝，不更新时间
    # 注意这里“不更新时间”很重要——如果你每次拒绝也更新时间，
    # 冷却就永远刷不完，相当于一旦被拦下就再也触发不了了
    return False
